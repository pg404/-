/*
组个最小数 (20)

题目描述
给定数字0-9各若干个。你可以以任意顺序排列这些数字，但必须全部使用。目标是使得最后得到的数尽可能小（注意0不能做首位）。例如：

给定两个0，两个1，三个5，一个8，我们得到的最小的数就是10015558。



现给定数字，请编写程序输出能够组成的最小的数。

输入描述:
每个输入包含1个测试用例。每个测试用例在一行中给出10个非负整数，顺序表示我们拥有数字0、数字1、……数字9的个数。整数间用一个空

格分隔。10个数字的总个数不超过50，且至少拥有1个非0的数字。


输出描述:
在一行中输出能够组成的最小的数。

输入例子:
2 2 0 0 0 3 0 0 1 0

输出例子:
10015558


*/ 

#include <iostream>
using namespace std;
int main(){
	int a[10];//用一个数组来存放”0~~9“的个数 
	bool flag = true;
	
	for(int i = 0; i < 10; i++){//第一个for循环输出首位，除0以外的最小那个数，然后把那个数字的个数减一 
		cin>>a[i];
		if(flag&&i!=0&&a[i]!=0){
			cout<<i;
			a[i] = a[i] -1;
			flag = false;
		}
	}
	
	for(int i = 0; i < 10; i++){//第二个for循环直接遍历数组，因为已经控制首位了，所以直接从小到大输完即可 
		for(int j = 0; j < a[i]; j++){
			cout<<i;
		} 
	}
	return 0;
} 


